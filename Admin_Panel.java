        Database bt = new Database();
        
        try {
            bt.getConnection();
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Panel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            bt.check();
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Panel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        SimpleDateFormat date_form = new SimpleDateFormat("yyyy-MM-dd");
        
        event_name = txt_event_name.getText().toString();
        date = date_form.format(jCalendar1.getDate());
        ticket=Integer.parseInt(txt_tickets.getValue().toString());
        
        if (ticket <= 0) {
            try {
                throw new ArithmeticException();
            } catch (ArithmeticException e) {
                JOptionPane.showMessageDialog(this, "PLEASE RE-ENTER VALID TICKET COUNT");
                txt_tickets.setValue(0);
                //System.exit(1);
            }
        }
        
        try {
            bt.Create_Event(event_name, date, ticket,admin_id);
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Panel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        System.out.println(event_name);
        System.out.println(date);
        System.out.println(ticket);
        
