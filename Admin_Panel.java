                String tickets = String.valueOf(rs.getInt("numb_of_ticket"));
                String event_name =rs.getString("event_name");
                String event_date = rs.getString("event_date");
                String seating = rs.getString("seating_portion");
                String event_id = String.valueOf(rs.getInt("event_id"));
                
                
                String tbdata[] ={checkout_id,user_id,admin_id,user_name,tickets,event_name,event_date,seating,event_id};
